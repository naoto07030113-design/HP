export function getTodaysTopic() {
  const today = new Date()
  const start = new Date(today.getFullYear(), 0, 0)
  const dayOfYear = Math.floor((today - start) / (1000 * 60 * 60 * 24))
  const allTopics = Object.values(TOPIC_SUGGESTIONS).flat()
  return allTopics[dayOfYear % allTopics.length]
}

export function getWeeklyTopics() {
  const today = new Date()
  const start = new Date(today.getFullYear(), 0, 0)
  const dayOfYear = Math.floor((today - start) / (1000 * 60 * 60 * 24))
  const allTopics = Object.values(TOPIC_SUGGESTIONS).flat()
  return Array.from({ length: 7 }, (_, i) => ({
    topic: allTopics[(dayOfYear + i) % allTopics.length],
    date: new Date(today.getTime() + i * 86400000),
  }))
}
